-- --------------
-- DATE_DIFF(): 
-- --------------
SELECT name, birthdate, DATEDIFF(NOW(), birthdt) AS 'Days' FROM people;
+-------+------------+-------+
| name  | birthdate  | Days  |
+-------+------------+-------+
| Larry | 2000-04-10 |  7802 |
| Barry | 1968-04-10 | 19490 |
| Alex  | 2021-08-20 |     0 |
+-------+------------+-------+
-- Check Doc: 
https://dev.mysql.com/doc/refman/8.0/en/date-and-time-functions.html#function_datediff 

-- --------------
-- DATE_ADD(): 
-- --------------

SELECT birthdt, DATE_ADD(birthdt, INTERVAL 1 MONTH) FROM people;
+---------------------+-------------------------------------+
| birthdt             | DATE_ADD(birthdt, INTERVAL 1 MONTH) |
+---------------------+-------------------------------------+
| 2000-04-10 08:30:26 | 2000-05-10 08:30:26                 |
| 1968-04-10 03:59:30 | 1968-05-10 03:59:30                 |
| 2021-08-20 05:15:38 | 2021-09-20 05:15:38                 |
+---------------------+-------------------------------------+ 

-- --------------
-- +/-: 
-- --------------

SELECT birthdt, birthdt + INTERVAL 1 MONTH FROM people; 
+---------------------+----------------------------+
| birthdt             | birthdt + INTERVAL 1 MONTH |
+---------------------+----------------------------+
| 2000-04-10 08:30:26 | 2000-05-10 08:30:26        |
| 1968-04-10 03:59:30 | 1968-05-10 03:59:30        |
| 2021-08-20 05:15:38 | 2021-09-20 05:15:38        |
+---------------------+----------------------------+

SELECT birthdt, birthdt - INTERVAL 5 MONTH FROM people; 
+---------------------+----------------------------+
| birthdt             | birthdt - INTERVAL 5 MONTH |
+---------------------+----------------------------+
| 2000-04-10 08:30:26 | 1999-11-10 08:30:26        |
| 1968-04-10 03:59:30 | 1967-11-10 03:59:30        |
| 2021-08-20 05:15:38 | 2021-03-20 05:15:38        |
+---------------------+----------------------------+

